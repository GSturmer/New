<h1>Complexity</h1>


<div><img src="images/complexity.jpg" style="margin:10px;width:100%" alt="Complexity in Transparency and Traceabiity Systems"/></div>
<p class="p1">
These are four forms of complexity that have inherent brand risk and thus require a mention.
<br/><br/>
The Outsource.
<br/><br/>
When your supplier decides to outsource something that you think he is doing in house your whole compliance scheme can fall apart. Most traceability systems simply cannot handle such an issue simply.
<br/><br/>
The Re-Assign
<br/><br/>
This means your supplier takes somethingthat was originally in production for someone else under their rules and re-assigns it be be under your rules. This is hard to avoid without traceability. Commercial pressure with drive these occurences and once again they can have extremely unfortunate repurcussions on your image and the quality of your products.  A good system can make these problems almost disappear using Time, Sales Order and purchase information.
<br/><br/>
The Fix
<br/><br/>
This means aa process is stopped additions are made and the process then continued. Not really succh aa problem if you have a good restricted chemical policy in place but of note because the impact of a fix is to change the environmental results of a batch and can often deliver lower quality items.
<br/><br/>
The Rework
<br/><br/>
Like the fix only done after the process is completed, it has the added issue that it can often be outsourced or contracted in as well.
<br/><br/>
These complexities are of note because theey imply the need for a creative or on the fly element to the traaceability and acccounting system to deal with production problems. In fact you could easily say that without such a creative element any traceability system will fail and the only possible response is to revert to lower levels of data and generic procurement information.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-19">
<h1>Creativity</h1>
<p class="p1">
So in the end it all comes down to flexibility.  A system has to allow a company to do what it wants to do, and to record such events accurately if it is to be of any use to the management.  There are  many natural products that need quality adjustments throughout the entire production process.   There are also creative processes themselves. Unless a system covers these it is flawed. It won't always matter in terms of auditing but ultimately it is a fools errand to operate at a level of transparency you cannot maintain across the supply chain in all circumstances cheaply.  In fact you need an ROI for your transparency system and you are really unlikely to get it if the data input side is too pricey or does not deliver savings and efficiencies.  At the same time if you don't actually know what a transparent system can do for your company then you are not in position to decide a suitable level of implementation and you are missing a trick. Transparency is not a state of being, it is a management tool and it depends entirely on the shareholders which of its many benefits needs to be delivered by the management.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 4-1">
<h1>Transparency Limits of benefit </h1>
<p class="p1">
It may seem odd that and avocate of transparency is also cautioning about the beneficial limits of transparency, however on closer inspectiion you'll see that this is an implementation and optimal performance issue rather than a capability issue. In short the system has to be capable of delivering [3,3] level transparency before you can use it in a targetted way.  There are well researched papers that indicate that extreme transparency can come at the expense of creativity, which is great if you have processes which needs to be repeated exactly, but not so great if you wish to see continual improvement.   It does however turn out that you can introduce boundaries of transparency using our already defined model. Boundaries in time, physically in space, and virtually in observation and recording which can then be used to increase the efficiency of the creative process. To put this another way you need to set your transparency metrics differently for R&D and production.  If you can't do this because your system can't handle it  you cannot optimise your transparency or ultimately your business.  The following is a well researched example of why this is important. <a href="https://hbr.org/2014/10/the-transparency-trap" target="blank">Havard Business Review</a>
<br/><br/>
The article covers practical issues that affect productivity due to the perceived level of transparency succintly, but these are not the only issues that you need to consider.  There are marketing and Brand reputational value issues that must be treated in the same way as all other business decisions. And crucially you need to look at your marketplace and understand your customer expectations and your competitor position. Transparency is not the elixir of life it is often made out to be, both too much and too little can harm the business. The main issue is the lack of clearlly defined metrics in the consultancy marketspace which allows allsorts of claims to be made. Which ever way you look at it Transparency can no longer be ignored safely and the best possible defence a management can have is to implement a fully capable systems and then decide in consultation with it's shareholders which level of metric is most viable in the medium term</p>
