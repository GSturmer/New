<h1>The Tool and Process Expansion</h1>

 <div><img src="images/tools.jpg" style="margin:10px;width:100%" alt="Corporate Framework for Transparency and Traceabiity Systems"/></div>
 <p class="p 1">
	Our goal in these tutorials is rather different to that of either the system developer or the Certification body.  We are seeking to prove metrics of Transparency are possible, and then consequently use them to guide the development of a management tool.   This tool can be thought of as
	an adjustable spanner. A tool that can be used on many different sizes of nuts. The developer is much more interested in longevity and single use tools that keep you coming back again and again. If you have been following to this point you will hopefully now be in a position to define the requirements of our tool as being those which allow the collection and transmission of data for all aspects of the following diagram.</p>

 <div><img src="images/ProcessF.jpg" style="margin:10px;width:100%" alt="Transparency Requirements"/></div>
 <p class="p1">
	The only thing that we need to remember at this stage is that there is further complexity and there is a reason why the system derived is called Theseus. Here we have described a short straight supply chain, however when we model a process in time and space there is no reason why we should only connect it to other processes inside the same entity. If we model it correctly and use a proper traceability algorythm then we should be able to connect it to all its antecedents where ever they occur.  Once we do that then just like the wool that Theseus left behind himself in the Labarynth, there is no problem in moving up down backwards forwards sideways or even looping in the supply chain because you can always just pay out more wool so you can always find your way back out. In the end you may not always be taking the most efficient path, but that's the point isn't it.  Once you know the route you can always improve it. <br/><br/>
	At this point we will do something that will annoy any IT department. We will expand our process model so that we have sufficient classes of inputs and outputs to ensure simple compliance information is easily managed at the most basic process level.  If you do wish to argue that less classes of data are required then feel free to do so, in your own field your most likely correct. The thing is the system is designed for use in any field and also to be used to train and support people regardless of their educational background. Simplicity ensures that they can perform complex functions within any certification regime without prior knowledge. So we expand the classes in the process model to allow complexity to be taken care of by the computer.</div>

<div class=" ui-content jqm-content hiddenContent 3-2">
<h1>Time</h1>
<p class="p1">
Perhaps the most important of the expansions is Time. Obviously the process time will have an effect on process energy etc, but there is far more going on under the hood in terms of how time can be used for auditing transparency across a supply chain. <br/><br/>  It is a simple fact that as soon as a supply chain traceability platform is in place time can be used to verify existance. Time defines when something happened and how long it took to happpen and since things happen in a regular organised manner in production you can spot fakes easily. <br/><br/>
A cow is pregnant for a set time  and faking a cow takes as long as making a cow if you set your system correctly.  Now it may be possible for one company to fake time records, but it becomes extremely difficult for two companies to do so and an order of magnitude more complex for three. The facts are that a transparent supply chain self audits according to time. <br/><br/> Time is and can also be used for many other purposes, such as validation of worker productivity or pay. Now any sound company needs to do that, but how many actually realise that if you relate workers to pay to hours to production batches you can eradicate child labour.  Obviously you would need to want to eradicate slave and child labour but it's absolutely possible using time based calculations and a knowledge of how long things usually take to produce. So all you actually need is that central supply line we defined earlier and an associated Bill of Processes and everythig becomes self auditing.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-3">
<h1>Location</h1>
<p class="p1">
The location expansion is not so obvious but it is extremely useful, particularly in schemes like HACCP.  It basically ensures that every process is a critical process. To do this we have extended a virtual and or a real input and output warehouse location on either side of every process. (these are siometimes called in-line warehouses)   Their presence in the traceability algorhythm allows us to ignore the corporate boundary should we so wish and to "teleport" anywhere in the supply chain with just the addition of a creative process, or a delivery note. Again whilst this ability might only ever be used in the exception, the fact that it closes the supply chain means inputs and outputs cannot simply appear or disappear without being noted as doing so. Other benefits include each individual process being an auditable microcosm of the company itself allowing value ladders, Batch P/L or accurate WIP calculations to be drawn. The location also allows knowledge of where a batch for a Sales order is in production at any time automatically if required. <br/><br/>In a lot of chemical storage situations location is used to reduce risk simply by ensuring chemicals which combine explosively and fire hazards etc are never stired together.<br/><br/> So by having a location at the start and at the end of a process we get a flexibility in modelling that is otherwise not present.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-4">
<h1>Inputs and Outputs</h1>
<p class="p1">
Inputs and Outputs seem fairly simple at first glance but actually as I mentioned earlier we will expand both of these and mirror them.  What do I mean.  Well taking inputs as an example every chemical formula or receipe has two classes of input. A key input and other inputs. The key input is the input used as the base for calculations. So you might need 2 eggs and 400grams of flour for a cake. Either ingredient can be the key input but if you are a chicken farmer your key input would most likely be eggs and if you are a wheat farmer it would most likely be flour.   Either way if you have 4 eggs you'll need 800g of flour to bake a cake that is twice the size.  Again if our traceability is to speak simply a Key input has a matched Key output and in this way we can define the central supply line. By doing so our metric can be easily measured by the number of key inputs or outputs we can define should we wish.
<br/><br/>
This is not the only expansion for inputs and outputs.  If you actually want to be able to speak process you will need six types of inputs and outputs within the two classes.  These are Materials (legally required for audit) Sub-products (secondary Central supply line info) Products (legally required at audit) Rejects (highly useful for resource efficiency) Waste (highly useful for environmental monitoring and resource efficiency) and Water (almost every environmental scheme checks this separately so you should get used to it). Each of these types caan be either a Key input or Other input in a process or indeed a Key or other output from a process and between them you will discover that you can maintain compliance matricies for any scheme.  Most information systems leave the categorisation to the item level which basically creates huge amounts of work and cost in a multibrand - multiproduct compliance environment.
</p>

</div>

<div class=" ui-content jqm-content hiddenContent 3-5">
<h1>Sub-products</h1>
<p class="p1">
The simplest description of a sub-product is an something that can only be saleable with further processing.  Many systems have this totally wrong because they are unable to send a product back into production. They leave mid production items that can be sold as sub-products, this is confusing, hard to audit and un-helpful for Sales people managers and production people alike.  So we use sub-products to deal with multiple outputs from the same process that all need further processing.  At the same time the key sub-product is the sub-product defined in Transparency Metrics for the supply chain we are considering.
</P>
</div>

<div class=" ui-content jqm-content hiddenContent 3-6">
<h1>Products</h1>
<p class="p1">
Product has a legal meaning.  It means something that has been made ready for sale.  Even if you are just a warehouse you bring in Materials and you sell Products in your accounts and the transformation is a process.  It could be a simple process such as putting the item out on display or it could be a huge chain of processes with many sub-products inbetween either way its still a transformation that has to be defined in the balance sheet and P/L accurately. <br/><br/>
As mentioned above a product is something that can be sold, but our system also allows it to continue in production to another product. This is both legal and sensible if the input and output warehouses are available.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-7">
<h1>Waste</h1>
<p class="p1">
There is nothing finer in terms of environmental management and resource efficiency than taking a waste and turning back into a product, a sub-product or an energy source. The Waste extension allows everyone to focus on the issue.  It is also a fact that where there are emissions they need to be put into a type and since they cannot be sold, and cannot be further processed they are clearly in need of a separate type.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-8">
<h1>Rejects</h1>
<p class="p1">
A reject may or may not become waste, it is simply an item that is un-fit to be included in a delivery against a specific salesorder, so it's not a product. By having this type separated it again highlights and focuses everyone on efficiency.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-9">
<h1>Water</h1>
<p class="p1">
Really this is just another input but it's such an important one in so many certifications that it is just better and easier to give it its own type.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-10">
<h1>Equipment</h1>
<p class="p1">
Whether you use equipment in processes to define the depreciation or the power consumption the facts are that most machines actually wear out with use, so it is a reasonably comfortable extension to detail anything that could be described as a tool in sufficient detail for all possible purposes during its use in processes. The implications for Health and Safety are not so clear but they arise from putting the machine into the process rather than actual use.  Sound crazy, but the fact is location creates risk regardless of whether the machine or tool is actuaally switched on or no.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-11">
<h1>Consumables</h1>
<p class="p1">
The consumable extentsion is basically there to be used to highlight things that go into processes and don't come out inside sub-products or products.  Normally useful for maintenance processes and for highlighting sources of emissions, they also are of interest in terms of efficiency and wastefulness.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-12">
<h1>People</h1>
<p class="p1">
Unless a process is fully automatic a person will be involved, this then is a key area of compliance. A process model that is incapable of detailing people at work is not much of a process model at all anymore, but all too often you will see people in processes being represented in terms of only their direct cost and productivity. There is so much more you can do and in reality productivity is related to training and morale. And morale is related to how well the company is treating it's people.  You baasically have a virtuous or a vicious circle in play at aany time.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-13">
<h1>Instructions and Controls</h1>
<p class="p1">
The thing about including instructions and controls at the process level is that it becomes absolutely clear where the limit of compliance and hence liability sits between the worker and his or her company. It is also a very useful way to ensure QC systems are being honoured. ISO systems require information to be maintained at this level so it's a good idea to have built in capabilities at the start.
</p>
</div>
<div class=" ui-content jqm-content hiddenContent 3-14">
<h1>Conservation of Mass</h1>
<p class="p1">
We have already ensured that things can't easily just appear and disappear in our supply chain but obviously if we apply conservation of mass at the batch process level everything becomes transparent. Why? Because it highlights losses into the environment, located personnel based losses such as theft and hiding at the point of occurence right across a supply chain making it extremely difficult to fake anything.  If you mass balance in real time and link to the financial data it basically becomes almost impossible to commit all types of fraud undetected. Over-paying for goods or services can still continue, but that is the only fraud that can and its easy to spot and stop once PO data is available.
</p>
</div>

<div class=" ui-content jqm-content hiddenContent 3-15">
<h1>Conservation of energy</h1>
<p class="p1">
At the end of the day CO2 footprints are basically a waste of time to everyone but the scientist trying to reduce costs. Well that is not really set to change except in regard to the complete impossibility of faking data, and the total possibility of automated auditing that results from a Mass Moneey aand Energy Balance Sheet.  Most, but not all, industrial environmental compliance schemes have an energy component so it's good to have it available in any case.
</p>
</div>

