    <h3>Linking Traceability and Transparency</h3>

    <p class="p1">

	The most critical step in understanding Transparency is understanding that without Traceabiility, Transparency is impossible in the globalized digital world. So it is entirely possible to have a company with open books that has simply moved all it's bad practices
	one step down the supply chain if the company it contracts with has closed books.  This may sound a bit extreme but it actually explains and is the cause of all the continued defforestation that can be attributed to the orders placed by global multinationals.
	<br></br>
	What happens is the orders are fed to a separate company and the corporate veil and a lack of traceability and transparency ensures that the value proposition is sound.  As soon as someone like Greenpeace defines the traceability as being directly attributable to the ccompany and defines the consequences
	of the processes the value proposition no longer holds and the process is either moved through  another node in the supply chain or changed. Fact is this is also the chosen method for the tax avoider and money launderer so
	it is extremly odd that governments have done little if anything at all, to close the loop holes during a period of intense globalization and technological development that has made the whole process much easier.
	<br/><br/>
	What these tutorials will absolutely demonstrate beyong any possible argument is that the technology and connectivity is already freely available to stop this nonsense.  It is both simple
	and cheap to establish mechanisms that make it impossible to cheat and defraud shareholders and stakeholders but that they are not employed because auditing legislation is behind the curve.
	<br/><br/>
